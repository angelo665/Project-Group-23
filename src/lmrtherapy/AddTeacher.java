    private void CbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbtnActionPerformed
        String userName, fullName, Password, query;
        String SUrl, SUser, SPass;
        SUrl = "jdbc:mysql://localhost:3306/lmrtherapycenter";
        SUser = "root";
        SPass = "";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st =con.createStatement();
            if("".equals(Uname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Username is required", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(Fname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Fullname is required", "Error",
                        JOptionPane.ERROR_MESSAGE);
                }else if("".equals(password.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Password is required", "Error",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                    userName =Uname.getText();
                    fullName =Fname.getText();
                    Password = password.getText();
                    System.out.println(Password);
                    query = "INSERT INTO admin (username, fullname, password)"+
                            "VALUES ('"+userName+"', '"+fullName+"', '"+Password+"')";
                    st.execute (query);
                    Uname.setText("");
                    Fname.setText("");
                    password.setText("");
                    
                    showMessageDialog(null,"Account has been created successfully");
                    
                    this.dispose();
                    
                }
        }catch(HeadlessException | ClassNotFoundException | SQLException e){
            System.out.println("Error!" + e.getMessage());
        }
        
    }//GEN-LAST:event_CbtnActionPerformed

